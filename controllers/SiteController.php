    public function actionCheck(){
        $reminder = Reminder::find()->asArray()->all();
        
        //var_dump($reminder);
        for($i=0;$i<sizeof($reminder);$i++){
            if($reminder[$i]['state'] == '1'){
                if($reminder[$i]['tgl_survei'] == date("Y-m-d H:i")){
                    $this->actionSend();
                }
            }
        }
        //if(date("Y-m-d H:i:s") == )
    }
}
